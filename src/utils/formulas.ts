const getFamily = (jodi: string): string[] => {
  const rashiMap: Record<string, string> = {
    '0': '5', '1': '6', '2': '7', '3': '8', '4': '9',
    '5': '0', '6': '1', '7': '2', '8': '3', '9': '4'
  };
  const a = jodi[0];
  const b = jodi[1];
  const ar = rashiMap[a];
  const br = rashiMap[b];
  return [a+b, b+a, a+br, br+a, ar+b, b+ar, ar+br, br+ar];
};

export const calculatePrediction = (
  inputs: PredictionInput,
  selectedFormulas: string[],
  pastMurda: string[] = [],
  currentMonthNums: string[] = [],
  todaysRes: string[] = [],
  past4DaysMurda: string[] = []
): PredictionResult => {
  const dateObj = new Date(inputs.date);
  const jsDay = dateObj.getDay();
  const dayOfMonth = dateObj.getDate();
  
  const jodiScores: Record<string, number> = {};
  const scoreBreakdown: Record<string, string[]> = {};
  const rawList: string[] = [];

  todaysRes.forEach(r => {
    let key = r.trim();
    if (key === '00') key = '100';
    else key = parseInt(key, 10).toString().padStart(2, '0');
    if (MASTER_SHEET[key]) {
      rawList.push(...MASTER_SHEET[key]);
    }
  });

  const counts: Record<string, number> = {};
  rawList.forEach(num => {
    counts[num] = (counts[num] || 0) + 1;
  });

  const outerHaruf = parseInt(dayOfMonth.toString().slice(-1));
  const rashi = (outerHaruf + 5) % 10;

  const LOGIC_3_JORIS = ['30', '03', '41', '14', '74', '47', '85', '58', '96', '69'];

  Object.keys(counts).forEach(jodi => {
    let score = counts[jodi];
    let details = [`Base(${counts[jodi]})`];

    if (selectedFormulas.includes('6')) {
      if (pastMurda.includes(jodi)) { score += 10; details.push("Murda(+10)"); }
      else {
        let isMurFam = false;
        for (const pm of pastMurda) {
          if (getFamily(pm).includes(jodi)) { isMurFam = true; break; }
        }
        if (isMurFam) { score += 6; details.push("MurFam(+6)"); }
      }
    }

    if (selectedFormulas.includes('3') && [1,2,3].includes(dayOfMonth) && UNIVERSAL.includes(jodi)) { score += 10; details.push("Univ(+10)"); }
    if (selectedFormulas.includes('4') && MAGIC.includes(jodi)) { score += 15; details.push("Magic(+15)"); }
    if (selectedFormulas.includes('7') && (jodi.includes(outerHaruf.toString()) || jodi.includes(rashi.toString()))) { score += 5; details.push("Haruf(+5)"); }
    if (selectedFormulas.includes('5') && (DAY_WISE_FIXED[jsDay] || []).includes(jodi)) { score += 5; details.push("DayFix(+5)"); }

    if (selectedFormulas.includes('8')) {
      const jodiNum = parseInt(jodi);
      const baki = 100 - (jodiNum === 0 ? 100 : jodiNum);
      const bakiStr = baki === 100 ? '00' : baki.toString().padStart(2, '0');
      if (pastMurda.includes(bakiStr) || MAGIC.includes(bakiStr)) { score += 3; details.push("Baki(+3)"); }
    }

    if (selectedFormulas.includes('2') && EVERGREEN.includes(jodi[0]) && EVERGREEN.includes(jodi[1])) { score += 7; details.push("EverG(+7)"); }
    if (selectedFormulas.includes('9') && currentMonthNums.includes(jodi)) { score += 3; details.push("MonthTrend(+3)"); }

    if (selectedFormulas.includes('10')) {
      const isLogic3Active = LOGIC_3_JORIS.some(j => past4DaysMurda.includes(j));
      if (isLogic3Active && LOGIC_3_JORIS.includes(jodi)) {
        score += 3;
        details.push("Logic3(+3)");
      }
    }

    jodiScores[jodi] = score;
    scoreBreakdown[jodi] = details;
  });

  const filteredJodis: Record<string, number> = jodiScores;


  const sortedJodis = Object.keys(filteredJodis).sort((a, b) => filteredJodis[b] - filteredJodis[a]);
  const final30 = sortedJodis.slice(0, 30);

  const l1 = final30.slice(0, 4);
  const l2 = final30.slice(4, 14);
  const l3 = final30.slice(14, 30);

  const tokariItems: TokariItem[] = [];
  const processed = new Set<string>();

  final30.forEach(jodi => {
    if (processed.has(jodi)) return;
    const reversed = jodi.split('').reverse().join('');
    let id = '';
    let jodisList: string[] = [];
    if (jodi === reversed) {
      id = jodi; jodisList = [jodi]; processed.add(jodi);
    } else {
      const min = parseInt(jodi) < parseInt(reversed) ? jodi : reversed;
      const max = parseInt(jodi) > parseInt(reversed) ? jodi : reversed;
      id = `${min}/${max}`; jodisList = [min, max];
      processed.add(min); processed.add(max);
    }
    const count = counts[jodi] || 0;
    tokariItems.push({ id, jodis: jodisList, count });
  });

  return { l1, l2, l3, tokari: tokariItems.sort((a, b) => b.count - a.count) };
};
